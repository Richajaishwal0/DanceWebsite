let apply=document.querySelector(".apply");
function showForm()
{
    if (apply.style.display=="none")
    {
        apply.style.display="block";
    }
    else{
        apply.style.display="none";
    }

}
function Greet()
{   
    if (apply.style.display=="none")
    {
        apply.style.display="block";
    }
    else{
        apply.style.display="none";
    }
    alert("Thanks for filling up the form. We will contact you!!")
}